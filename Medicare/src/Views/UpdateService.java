    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:

        if (getService.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Service Name In Capitals");
            return; // or throw an Exception
        }
        if (getCharges.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Charges");
            return; // or throw an Exception
        }

        try {
            charges = Integer.parseInt(getCharges.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showConfirmDialog(null, "Please enter numbers only In Charges ", "Caution", JOptionPane.CANCEL_OPTION);
            return;
        }

        service = getService.getText().toString();

        Vector<String> addServiceVector = new Vector<String>();
        addServiceVector.add(service);
        addServiceVector.add(charges+ "");
        addServiceVector.add("Update");
        addServiceVector.add("Delete");
        for(int i=0;i<obj.getColumnCount();i++)
        {
            obj.setValueAt(addServiceVector.get(i), rowNumber, i);
        }
         JOptionPane.showMessageDialog(this, "Service Added Successfully !");
        this.dispose();

    }//GEN-LAST:event_okActionPerformed


